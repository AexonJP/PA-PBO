    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed
        if (username_TXT.getText().isEmpty() || pass_TXT.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Inputan tidak boleh kosong", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else {
            try {
                conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/anime", "root", "");
                String sql = "SELECT * FROM tb_admin WHERE username=? AND password=?";
                pst = conn.prepareStatement(sql);
                pst.setString(1, username_TXT.getText());
                pst.setString(2, pass_TXT.getText());
                rs = pst.executeQuery();

                if (rs.next()) {
                    JOptionPane.showMessageDialog(null, "Login Successful!");
                    username_TXT.setText("");
                    pass_TXT.setText("");
                    crudform CRUD = new crudform();
                    CRUD.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "Invalid Username or Password!");
                }

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
    }//GEN-LAST:event_btn_loginActionPerformed

